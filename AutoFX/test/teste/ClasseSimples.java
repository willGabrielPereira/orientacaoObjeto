        return "ClasseSimples{" + "inte=" + inte + ", doub=" + doub + ", floa=" + floa + ", lon=" + lon + ", shor=" + shor + ", byt=" + byt + ", boo=" + boo + ", carac=" + carac + ", palav=" + palav + '}';
